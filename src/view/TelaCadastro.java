    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        Clientes c = new Clientes();
        ClienteDAO dao = new ClienteDAO();
        c.setNome(txtNome.getText());
        c.setCpf(Integer.parseInt(txtCpf.getText()));
        c.setEmail(txtEmail.getText());
        c.setSenha(txtSenha.getText());
        c.setEndereco(txtEndereco.getText());
        c.setTelefone(Integer.parseInt(txtTelefone.getText()));
        dao.creat(c);
        txtNome.setText("");
        txtCpf.setText("");
        txtEmail.setText("");
        txtSenha.setText("");
        txtEndereco.setText("");
        txtTelefone.setText("");
        readJTable();

    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed

        if (jtClientes.getSelectedRow() != -1) {
            Clientes c = new Clientes();
            ClienteDAO dao = new ClienteDAO();

            c.setId((int) jtClientes.getValueAt(jtClientes.getSelectedRow(), 0));
            dao.delete(c);

            txtNome.setText("");
            txtCpf.setText("");
            txtEmail.setText("");
            txtSenha.setText("");
            txtEndereco.setText("");
            txtTelefone.setText("");

            readJTable();
        }


    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        if (jtClientes.getSelectedRow() != -1) {

            Clientes c = new Clientes();
            ClienteDAO dao = new ClienteDAO();

            c.setNome(txtNome.getText());
            c.setCpf(Integer.parseInt(txtCpf.getText()));
            c.setEmail(txtEmail.getText());
            c.setSenha(txtSenha.getText());
            c.setEndereco(txtEndereco.getText());
            c.setTelefone(Integer.parseInt(txtTelefone.getText()));
            c.setId((int) jtClientes.getValueAt(jtClientes.getSelectedRow(), 0));
            dao.update(c);
            txtNome.setText("");
            txtCpf.setText("");
            txtEmail.setText("");
            txtSenha.setText("");
            txtEndereco.setText("");
            txtTelefone.setText("");
            readJTable();
        }
    }//GEN-LAST:event_jButton3ActionPerformed

    private void jtClientesMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jtClientesMouseClicked
        if (jtClientes.getSelectedRow() != -1) {

            txtNome.setText(jtClientes.getValueAt(jtClientes.getSelectedRow(), 0).toString());
            txtCpf.setText(jtClientes.getValueAt(jtClientes.getSelectedRow(), 1).toString());
            txtEmail.setText(jtClientes.getValueAt(jtClientes.getSelectedRow(), 2).toString());
            txtSenha.setText(jtClientes.getValueAt(jtClientes.getSelectedRow(), 3).toString());
            txtEndereco.setText(jtClientes.getValueAt(jtClientes.getSelectedRow(), 4).toString());
            txtTelefone.setText(jtClientes.getValueAt(jtClientes.getSelectedRow(), 5).toString());

        }
    }//GEN-LAST:event_jtClientesMouseClicked

